package com.lemeng.xiaobinbin.bean;

public class ShopItemBean {
	public long id = 0;
	public long costtype = 0;
	public long dealtype = 0;
	public String cost = "";
	public long parame = 0;
	public long desc = 0;
	public String 	icon = "";
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public long getCosttype() {
		return costtype;
	}
	public void setCosttype(long costtype) {
		this.costtype = costtype;
	}
	public long getDealtype() {
		return dealtype;
	}
	public void setDealtype(long dealtype) {
		this.dealtype = dealtype;
	}
	public String getCost() {
		return cost;
	}
	public void setCost(String cost) {
		this.cost = cost;
	}
	public long getParame() {
		return parame;
	}
	public void setParame(long parame) {
		this.parame = parame;
	}
	public long getDesc() {
		return desc;
	}
	public void setDesc(long desc) {
		this.desc = desc;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
	
	
	
}
